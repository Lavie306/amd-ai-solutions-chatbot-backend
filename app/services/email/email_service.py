"""
Email Service — gửi email qua SendGrid.

Hỗ trợ:
  - Email notify lead mới cho team AMD
  - Email follow-up tự động cho khách hàng
  - Template Jinja2 với placeholder {{name}}, {{contact}}, v.v.
"""
from __future__ import annotations

import asyncio
import logging
from typing import Any

from jinja2 import Template

from app.core.config import get_settings

logger = logging.getLogger(__name__)
settings = get_settings()


def _get_sendgrid():
    """Lazy import SendGrid — tránh crash khi chưa install."""
    try:
        from sendgrid import SendGridAPIClient
        from sendgrid.helpers.mail import Mail
        return SendGridAPIClient, Mail
    except ImportError:
        logger.warning("sendgrid package chưa được install — email bị tắt")
        return None, None


def render_template(template_str: str, context: dict[str, Any]) -> str:
    """Render Jinja2 template với context."""
    return Template(template_str).render(**context)


def send_email(
    to_email: str,
    subject: str,
    body_html: str,
    body_text: str | None = None,
) -> bool:
    """
    Gửi email qua SendGrid.
    Trả về True nếu thành công, False nếu lỗi.
    """
    if not settings.sendgrid_api_key:
        logger.warning("SendGrid API key chưa được cấu hình — bỏ qua gửi email")
        return False

    SendGridAPIClient, Mail = _get_sendgrid()
    if SendGridAPIClient is None:
        return False

    message = Mail(
        from_email=(settings.email_from, settings.email_from_name),
        to_emails=to_email,
        subject=subject,
        html_content=body_html,
        plain_text_content=body_text or body_html,
    )

    try:
        sg = SendGridAPIClient(settings.sendgrid_api_key)
        response = sg.send(message)
        logger.info(f"Email sent to {to_email} — status {response.status_code}")
        return response.status_code in (200, 201, 202)
    except Exception as e:
        logger.error(f"Failed to send email to {to_email}: {e}")
        return False


