DEFAULT_LEAD_NOTIFY_SUBJECT = "[AMD Lead] {{name}} — {{project_type}} — {{created_at}}"

DEFAULT_LEAD_NOTIFY_HTML = """
<h2>Có lead mới từ website chatbot</h2>
<table>
  <tr><td><b>Tên:</b></td><td>{{name}}</td></tr>
  <tr><td><b>Liên hệ:</b></td><td>{{contact}}</td></tr>
  {% for key, val in extra_fields.items() %}
  <tr><td><b>{{key}}:</b></td><td>{{val}}</td></tr>
  {% endfor %}
</table>
<h3>Tóm tắt hội thoại</h3>
<pre>{{chat_summary}}</pre>
<p><a href="{{dashboard_url}}">Xem lead trong dashboard →</a></p>
<hr>
<small>Auto-generated by AMD Website Chatbot</small>
"""

DEFAULT_FOLLOWUP_SUBJECT = "AMD AI Solutions — Theo dõi dự án {{project_type}}"

DEFAULT_FOLLOWUP_HTML = """
<p>Chào {{name}},</p>
<p>Trước đó anh/chị có liên hệ với AMD về dự án <b>{{project_type}}</b>.</p>
<p>AMD muốn hỏi thăm anh/chị đã cân nhắc thêm chưa ạ?</p>
<p>Nếu cần trao đổi thêm, anh/chị có thể reply email này hoặc nhắn Zalo <b>{{zalo_number}}</b>.</p>
<br>
<p>Trân trọng,<br>Team AMD AI Solutions</p>
"""


async def async_send_lead_notify(
    lead_id: int,
    name: str,
    contact: str,
    extra_fields: dict[str, Any],
    chat_log: list[dict],
    dashboard_base_url: str = "http://localhost:5173",
    custom_subject_template: str | None = None,
    custom_body_template: str | None = None,
) -> bool:
    loop = asyncio.get_event_loop()
    return await loop.run_in_executor(
        None,
        send_lead_notify,
        lead_id,
        name,
        contact,
        extra_fields,
        chat_log,
        dashboard_base_url,
        custom_subject_template,
        custom_body_template,
    )


async def async_send_followup_email(
    to_email: str,
    lead_data: dict[str, Any],
    template_str: str | None = None,
    subject_str: str | None = None,
    zalo_number: str = "",
) -> bool:
    loop = asyncio.get_event_loop()
    return await loop.run_in_executor(
        None,
        send_followup_email,
        to_email,
        lead_data,
        template_str,
        subject_str,
        zalo_number,
    )


def send_lead_notify(
    lead_id: int,
    name: str,
    contact: str,
    extra_fields: dict[str, Any],
    chat_log: list[dict],
    dashboard_base_url: str = "http://localhost:5173",
    custom_subject_template: str | None = None,
    custom_body_template: str | None = None,
) -> bool:
    """Gửi email thông báo lead mới cho team AMD."""
    from datetime import datetime

    chat_summary = "\n".join(
        f"{m['role'].upper()}: {m['content']}" for m in chat_log[-20:]
    )

    context = {
        "name": name or "Không rõ",
        "contact": contact or "Không rõ",
        "extra_fields": extra_fields,
        "chat_summary": chat_summary,
        "dashboard_url": f"{dashboard_base_url}/leads/{lead_id}",
        "project_type": extra_fields.get("project_type", "Chưa xác định"),
        "created_at": datetime.now().strftime("%d/%m/%Y %H:%M"),
    }

    subject = render_template(custom_subject_template or DEFAULT_LEAD_NOTIFY_SUBJECT, context)
    body = render_template(custom_body_template or DEFAULT_LEAD_NOTIFY_HTML, context)

    return send_email(
        to_email=settings.email_notify_to,
        subject=subject,
        body_html=body,
    )


def send_followup_email(
    to_email: str,
    lead_data: dict[str, Any],
    template_str: str | None = None,
    subject_str: str | None = None,
    zalo_number: str = "",
) -> bool:
    """Gửi email follow-up tự động cho khách hàng."""
    context = {**lead_data, "zalo_number": zalo_number}
    subject = render_template(subject_str or DEFAULT_FOLLOWUP_SUBJECT, context)
    body = render_template(template_str or DEFAULT_FOLLOWUP_HTML, context)
    return send_email(to_email=to_email, subject=subject, body_html=body)
